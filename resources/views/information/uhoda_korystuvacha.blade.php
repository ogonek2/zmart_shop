                        <h3>4. Порядок обробки персональних даних: отримання згоди, повідомлення про права та&nbsp;дії
                            з&nbsp;персональними даними суб’єкта персональних даних</h3>

                        <p>4.1. Згода суб’єкта персональних даних має бути добровільним волевиявленням фізичної особи щодо
                            надання дозволу на&nbsp;обробку її&nbsp;персональних даних відповідно до&nbsp;сформульованої
                            мети
                            їхньої обробки.</p>

                        <p>4.2. Згода суб’єкта персональних даних може бути надана у&nbsp;таких формах:</p>

                        <ul>
                            <li>документ на&nbsp;паперовому носії з&nbsp;реквізитами, що&nbsp;дає змогу ідентифікувати цей
                                документ і&nbsp;фізичну особу;</li>
                            <li>електронний документ, який має містити обов’язкові реквізити, що&nbsp;дають змогу
                                ідентифікувати
                                цей документ та&nbsp;фізичну особу. Добровільне волевиявлення фізичної особи щодо надання
                                дозволу на&nbsp;обробку її&nbsp;персональних даних доцільно засвідчувати електронним
                                підписом
                                суб’єкта персональних даних;</li>
                            <li>відмітка на&nbsp;електронній сторінці документа чи&nbsp;в&nbsp;електронному файлі,
                                що&nbsp;обробляється в&nbsp;інформаційній системі на&nbsp;основі документованих
                                програмно-технічних рішень.</li>
                        </ul>

                        <p>4.3. Згода суб’єкта персональних даних надається під час оформлення цивільно-правових відносин
                            відповідно до&nbsp;чинного законодавства.</p>

