                        <h3>6. Умови розкриття інформації про персональні дані третім особам</h3>

                        <p>6.1. Порядок доступу до&nbsp;персональних даних третіх осіб визначається умовами згоди суб'єкта
                            персональних даних, наданої володільцю персональних даних на&nbsp;обробку цих даних, або
                            відповідно
                            до&nbsp;вимог закону.</p>

                        <p>6.2. Доступ до&nbsp;персональних даних третій особі не&nbsp;надається, якщо зазначена особа
                            відмовляється взяти на&nbsp;себе зобов'язання щодо забезпечення виконання вимог Закону України
                            «Про
                            захист персональних даних» або неспроможна їх&nbsp;забезпечити.</p>

                        <p>6.3. Суб'єкт відносин, пов'язаних з&nbsp;персональними даними, подає запит щодо доступу
                            (далі&nbsp;—
                            запит) до&nbsp;персональних даних володільцю персональних даних.</p>

                        <p>6.4. У&nbsp;запиті зазначаються:</p>

                        <ul>
                            <li>прізвище, ім'я та&nbsp;по&nbsp;батькові, місце проживання (місце перебування)
                                і&nbsp;реквізити
                                документа, що&nbsp;посвідчує фізичну особу, яка подає запит (для фізичної особи&nbsp;—
                                заявника);</li>
                            <li>найменування, місцезнаходження юридичної особи, яка подає запит, посада, прізвище, ім'я
                                та&nbsp;по&nbsp;батькові особи, яка засвідчує запит; підтвердження того, що&nbsp;зміст
                                запиту
                                відповідає повноваженням юридичної особи (для юридичної особи&nbsp;— заявника);</li>
                            <li>прізвище, ім'я та&nbsp;по&nbsp;батькові, а&nbsp;також інші відомості, що&nbsp;дають змогу
                                ідентифікувати фізичну особу, стосовно якої робиться запит;</li>
                            <li>відомості про базу персональних даних, стосовно якої подається запит, чи&nbsp;відомості про
                                володільця чи&nbsp;розпорядника цієї бази персональних даних;</li>
                            <li>перелік персональних даних, що&nbsp;запитуються;</li>
                            <li>мета та/або правові підстави для запиту.</li>
                        </ul>

                        <p>6.5. Строк вивчення запиту на&nbsp;предмет його задоволення не&nbsp;може перевищувати десяти
                            робочих
                            днів з&nbsp;дня його надходження. Протягом цього строку володілець бази персональних даних
                            доводить
                            до&nbsp;відома особи, яка подає запит, що&nbsp;запит буде задоволене або відповідні персональні
                            дані
                            не&nbsp;підлягають наданню, із&nbsp;зазначенням підстави, визначеної у&nbsp;відповідному
                            нормативно-правовому акті. Запит задовольняється протягом тридцяти календарних днів з&nbsp;дня
                            його
                            надходження, якщо інше не&nbsp;передбачено законом.</p>

                        <p>6.6. Відстрочення доступу до&nbsp;персональних даних третіх осіб допускається у&nbsp;разі, якщо
                            необхідні дані не&nbsp;можуть бути надані протягом тридцяти календарних днів з&nbsp;дня
                            надходження
                            запиту. При цьому загальний термін вирішення питань, порушених у&nbsp;запиті, не&nbsp;може
                            перевищувати сорока п'яти календарних днів.</p>

                        <p>6.7. Повідомлення про відстрочення доводиться до&nbsp;відома третьої особи, яка подала запит,
                            у&nbsp;письмовій формі з&nbsp;роз'ясненням порядку оскарження такого рішення.</p>

                        <p>6.8. У&nbsp;повідомленні про відстрочення зазначаються:</p>

                        <ul>
                            <li>прізвище, ім'я та&nbsp;по&nbsp;батькові посадової особи;</li>
                            <li>дата відправлення повідомлення;</li>
                            <li>причина відстрочення;</li>
                            <li>строк, протягом якого буде задоволено запит.</li>
                        </ul>

                        <p>6.9. Відмова у&nbsp;доступі до&nbsp;персональних даних допускається, якщо доступ до&nbsp;них
                            заборонено згідно із&nbsp;законом.</p>

                        <p>6.10. У&nbsp;повідомленні про відмову зазначаються:</p>

                        <ul>
                            <li>прізвище, ім'я, по&nbsp;батькові посадової особи, яка відмовляє у&nbsp;доступі;</li>
                            <li>дата відправлення повідомлення;</li>
                            <li>причина відмови.</li>
                        </ul>

                        <p>6.11. Рішення про відстрочення або відмову із&nbsp;доступі до&nbsp;персональних даних може бути
                            оскаржено до&nbsp;суду.</p>

                        <p>&nbsp;</p>

                        <h3>7. Захист персональних даних: способи захисту, відповідальна особа, працівники, які
                            безпосередньо
                            здійснюють обробку та/або мають доступ до&nbsp;персональних даних у&nbsp;зв’язку
                            з&nbsp;виконанням
                            своїх службових обов’язків, строк зберігання персональних даних</h3>

                        <p>7.1. Володільця бази персональних даних обладнано системними і&nbsp;програмно-технічними засобами
                            та&nbsp;засобами зв’язку, які запобігають втратам, крадіжкам, несанкціонованому знищенню,
                            викривленню, підробленню, копіюванню інформації і&nbsp;відповідають вимогам міжнародних
                            та&nbsp;національних стандартів.</p>

                        <p>7.2. Відповідальна особа організовує роботу, пов’язану із&nbsp;захистом персональних даних при
                            їх&nbsp;обробці, відповідно до&nbsp;закону. Відповідальна особа визначається наказом Володільця
                            бази
                            персональних даних.</p>

                        <p>Обов’язки відповідальної особи щодо організації роботи, пов’язаної із&nbsp;захистом персональних
                            даних при їх&nbsp;обробці зазначаються у&nbsp;посадовій інструкції.</p>

                        <p>7.3. Відповідальна особа зобов’язана:</p>

                        <ul>
                            <li>знати законодавство України в&nbsp;сфері захисту персональних даних;</li>
                            <li>розробити процедури доступу до&nbsp;персональних даних співробітників відповідно
                                до&nbsp;їхніх
                                професійних чи&nbsp;службових або трудових обов’язків;</li>
                            <li>забезпечити виконання співробітниками Володільця бази персональних даних вимог законодавства
                                України в&nbsp;сфері захисту персональних даних та&nbsp;внутрішніх документів,
                                що&nbsp;регулюють
                                діяльність Володільця бази персональних даних щодо обробки і&nbsp;захисту персональних даних
                                у&nbsp;базах персональних даних;</li>
                            <li>розробити порядок (процедуру) внутрішнього контролю за&nbsp;дотриманням вимог законодавства
                                України в&nbsp;сфері захисту персональних даних та&nbsp;внутрішніх документів,
                                що&nbsp;регулюють
                                діяльність Володільця бази персональних даних щодо обробки і&nbsp;захисту персональних даних
                                у&nbsp;базах персональних даних, який, зокрема, повинен містити норми щодо періодичності
                                здійснення такого контролю;</li>
                            <li>повідомляти Володільця бази персональних даних про факти порушень співробітниками вимог
                                законодавства України в&nbsp;сфері захисту персональних даних та&nbsp;внутрішніх документів,
                                що&nbsp;регулюють діяльність Володільця бази персональних даних щодо обробки і&nbsp;захисту
                                персональних даних у&nbsp;базах персональних даних у&nbsp;термін не&nbsp;пізніше одного
                                робочого
                                дня з&nbsp;моменту виявлення таких порушень;</li>
                            <li>забезпечити зберігання документів, що&nbsp;підтверджують надання суб’єктом персональних
                                даних
                                згоди на&nbsp;обробку своїх персональних даних та&nbsp;повідомлення вказаного суб’єкта про
                                його
                                права.</li>
                        </ul>

                        <p>7.4. З&nbsp;метою виконання своїх обов’язків відповідальна особа має право:</p>

                        <ul>
                            <li>отримувати необхідні документи, у&nbsp;тому числі накази й&nbsp;інші розпорядчі документи,
                                видані Володільцем бази персональних даних, пов’язані із&nbsp;обробкою персональних даних;
                            </li>
                            <li>робити копії з&nbsp;отриманих документів, у&nbsp;тому числі копії файлів, будь-яких записів,
                                що&nbsp;зберігаються в&nbsp;локальних обчислювальних мережах і&nbsp;автономних комп’ютерних
                                системах;</li>
                            <li>брати участь в&nbsp;обговоренні виконуваних ним обов’язків організації роботи, пов’язаної
                                із&nbsp;захистом персональних даних при їх&nbsp;обробці;</li>
                            <li>вносити на&nbsp;розгляд пропозиції щодо покращення діяльності та&nbsp;вдосконалення методів
                                роботи, подавати зауваження та&nbsp;варіанти усунення виявлених недоліків у&nbsp;процесі
                                обробки
                                персональних даних;</li>
                            <li>одержувати пояснення з&nbsp;питань здійснення обробки персональних даних;</li>
                            <li>підписувати та&nbsp;візувати документи в&nbsp;межах своєї компетенції.</li>
                        </ul>

                        <p>7.5. Працівники, які безпосередньо здійснюють обробку та/або мають доступ до&nbsp;персональних
                            даних
                            у&nbsp;зв’язку з&nbsp;виконанням своїх службових (трудових) обов’язків зобов’язані дотримуватись
                            вимог законодавства України в&nbsp;сфері захисту персональних даних та&nbsp;внутрішніх
                            документів,
                            щодо обробки і&nbsp;захисту персональних даних у&nbsp;базах персональних даних.</p>

                        <p>7.6. Працівники, що&nbsp;мають доступ до&nbsp;персональних даних, у&nbsp;тому числі, здійснюють
                            їх&nbsp;обробку зобов’язані не&nbsp;допускати розголошення у&nbsp;будь-який спосіб персональних
                            даних, які їм&nbsp;було довірено або які стали відомі у&nbsp;зв’язку з&nbsp;виконанням
                            професійних
                            чи&nbsp;службових або трудових обов’язків. Таке зобов’язання чинне після припинення ними
                            діяльності,
                            пов’язаної з&nbsp;персональними даними, крім випадків, установлених законом.</p>

                        <p>7.7.Особи, що&nbsp;мають доступ до&nbsp;персональних даних, у&nbsp;тому числі, здійснюють
                            їх&nbsp;обробку у&nbsp;разі порушення ними вимог Закону України «Про захист персональних даних»
                            несуть відповідальність згідно законодавства України.</p>

                        <p>7.8. Персональні дані не&nbsp;повинні зберігатися довше, ніж це&nbsp;необхідно для мети, для якої
                            такі дані зберігаються, але у&nbsp;будь-якому разі не&nbsp;довше строку зберігання даних,
                            визначеного згодою суб’єкта персональних даних на&nbsp;обробку цих даних.</p>

                        <p>&nbsp;</p>

                        <h3>8. Права суб’єкта персональних даних</h3>

                        <p>8.1. Суб'єкт персональних даних має право:</p>

                        <ul>
                            <li>знати про місцезнаходження бази персональних даних, яка містить його персональні дані,
                                її&nbsp;призначення та&nbsp;найменування, місцезнаходження та/або місце проживання
                                (перебування)
                                володільця чи&nbsp;розпорядника цієї бази або дати відповідне доручення щодо отримання цієї
                                інформації уповноваженим ним особам, крім випадків, встановлених законом;</li>
                            <li>отримувати інформацію про умови надання доступу до&nbsp;персональних даних, зокрема
                                інформацію
                                про третіх осіб, яким передаються його персональні дані, що&nbsp;містяться
                                у&nbsp;відповідній
                                базі персональних даних;</li>
                            <li>на&nbsp;доступ до&nbsp;своїх персональних даних, що&nbsp;містяться у&nbsp;відповідній базі
                                персональних даних;</li>
                            <li>отримувати не&nbsp;пізніш як&nbsp;за&nbsp;тридцять календарних днів з&nbsp;дня надходження
                                запиту, крім випадків, передбачених законом, відповідь про&nbsp;те, чи&nbsp;зберігаються
                                його
                                персональні дані у&nbsp;відповідній базі персональних даних, а&nbsp;також отримувати зміст
                                його
                                персональних даних, які зберігаються;</li>
                            <li>пред'являти вмотивовану вимогу із&nbsp;запереченням проти обробки своїх персональних даних
                                органами державної влади, органами місцевого самоврядування при здійсненні їхніх
                                повноважень,
                                передбачених законом;</li>
                            <li>пред'являти вмотивовану вимогу щодо зміни або знищення своїх персональних даних будь-яким
                                володільцем та&nbsp;розпорядником цієї бази, якщо ці&nbsp;дані обробляються незаконно
                                чи&nbsp;є&nbsp;недостовірними;</li>
                            <li>на&nbsp;захист своїх персональних даних від незаконної обробки та&nbsp;випадкової втрати,
                                знищення, пошкодження у&nbsp;зв'язку з&nbsp;умисним приховуванням, ненаданням
                                чи&nbsp;несвоєчасним їх&nbsp;наданням, а&nbsp;також на&nbsp;захист від надання відомостей,
                                що&nbsp;є&nbsp;недостовірними чи&nbsp;ганьблять честь, гідність та&nbsp;ділову репутацію
                                фізичної особи;</li>
                            <li>звертатися з&nbsp;питань захисту своїх прав щодо персональних даних до&nbsp;органів
                                державної
                                влади, органів місцевого самоврядування, до&nbsp;повноважень яких належить здійснення
                                захисту
                                персональних даних;</li>
                            <li>застосовувати засоби правового захисту в&nbsp;разі порушення законодавства про захист
                                персональних даних.</li>
                        </ul>

                        <p>&nbsp;</p>

                        <h3>9. Порядок роботи з&nbsp;запитами суб’єкта персональних даних</h3>

                        <p>9.1. Суб'єкт персональних даних має право на&nbsp;одержання будь-яких відомостей про себе
                            у&nbsp;будь-якого суб'єкта відносин, пов'язаних з&nbsp;персональними даними, без зазначення мети
                            запиту, крім випадків, установлених законом.</p>

                        <p>9.2. Доступ суб'єкта персональних даних до&nbsp;даних про себе здійснюється безоплатно.</p>

                        <p>9.3. Суб’єкт персональних даних подає запит щодо доступу (далі&nbsp;— запит) до&nbsp;персональних
                            даних володільцю бази персональних даних.</p>

                        <p>У&nbsp;запиті зазначаються:</p>

                        <ul>
                            <li>прізвище, ім'я та&nbsp;по&nbsp;батькові, місце проживання (місце перебування)
                                і&nbsp;реквізити
                                документа, що&nbsp;посвідчує особу суб’єкта персональних даних;</li>
                            <li>інші відомості, що&nbsp;дають змогу ідентифікувати особу суб’єкта персональних даних;</li>
                            <li>відомості про базу персональних даних, стосовно якої подається запит, чи&nbsp;відомості про
                                володільця чи&nbsp;розпорядника цієї бази;</li>
                            <li>перелік персональних даних, що&nbsp;запитуються.</li>
                        </ul>

                        <p>9.4. Строк вивчення запиту на&nbsp;предмет його задоволення не&nbsp;може перевищувати десяти
                            робочих
                            днів з&nbsp;дня його надходження. Протягом цього строку володілець бази персональних даних
                            доводить
                            до&nbsp;відома суб’єкта персональних даних, що&nbsp;запит буде задоволене або відповідні
                            персональні
                            дані не&nbsp;підлягають наданню, із&nbsp;зазначенням підстави, визначеної у&nbsp;відповідному
                            нормативно-правовому акті.</p>

                        <p>9.5. Запит задовольняється протягом тридцяти календарних днів з&nbsp;дня його надходження, якщо
                            інше
                            не&nbsp;передбачено законом.</p>

                        <p>&nbsp;</p>

                        <h3>10. Державна реєстрація бази персональних даних</h3>

                        <p>10.1. Державна реєстрація баз персональних даних здійснюється відповідно до&nbsp;статті
                            9&nbsp;Закону
                            України «<a href="http://zakon5.rada.gov.ua/laws/show/2297-17">Про захист персональних
                                даних</a>».
                        </p>

