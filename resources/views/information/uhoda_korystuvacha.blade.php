                        <h3>1. Загальні поняття та&nbsp;сфера застосування</h3>

                        <p>1.1. Визначення термінів:</p>

                        <p><b>база персональних даних</b>&nbsp;— іменована сукупність упорядкованих персональних даних
                            в&nbsp;електронній формі та/або у&nbsp;формі картотек персональних даних;</p>

                        <p><b>відповідальна особа</b>&nbsp;— визначена особа, яка організовує роботу, пов’язану
                            із&nbsp;захистом
                            персональних даних при їх&nbsp;обробці, відповідно до&nbsp;закону;</p>

                        <p><b>володілець бази персональних даних</b>&nbsp;— фізична або юридична особа, якій законом або
                            за&nbsp;згодою суб’єкта персональних даних надано право на&nbsp;обробку цих даних, яка
                            затверджує
                            мету обробки персональних даних у&nbsp;цій базі даних, встановлює склад цих даних
                            та&nbsp;процедури
                            їх&nbsp;обробки, якщо інше не&nbsp;визначено законом;</p>

                        <p><strong>Державний реєстр баз персональних даних</strong>&nbsp;— єдина державна інформаційна
                            система
                            збору, накопичення та&nbsp;обробки відомостей про зареєстровані бази персональних даних;</p>

                        <p><b>загальнодоступні джерела персональних даних&nbsp;—</b> довідники, адресні книги, реєстри,
                            списки,
                            каталоги, інші систематизовані збірники відкритої інформації, які містять персональні дані,
                            розміщені та&nbsp;опубліковані з&nbsp;відома суб’єкта персональних даних. Не&nbsp;вважаються
                            загальнодоступними джерелами персональних даних соціальні мережі та&nbsp;інтернет-ресурси,
                            в&nbsp;яких суб’єкт персональних даних залишають свої персональні дані (окрім випадків, коли
                            суб’єктом персональних даних прямо зазначено, що&nbsp;персональні дані розміщені з&nbsp;метою
                            їх&nbsp;вільного поширення та&nbsp;використання);</p>

                        <p><b>згода суб’єкта персональних даних</b>&nbsp;— будь-яке документоване, добровільне волевиявлення
                            фізичної особи щодо надання дозволу на&nbsp;обробку її&nbsp;персональних даних відповідно
                            до&nbsp;сформульованої мети їх&nbsp;обробки;</p>

                        <p><b>знеособлення персональних даних</b>&nbsp;— вилучення відомостей, які дають змогу
                            ідентифікувати
                            особу;</p>

                        <p><b>обробка персональних даних&nbsp;—</b> будь-яка дія або сукупність дій, здійснених повністю або
                            частково в&nbsp;інформаційній (автоматизованій) системі та/або в&nbsp;картотеках персональних
                            даних,
                            які пов’язані зі&nbsp;збиранням, реєстрацією, накопиченням, зберіганням, адаптуванням, зміною,
                            поновленням, використанням і&nbsp;поширенням (розповсюдженням, реалізацією, передачею),
                            знеособленням, знищенням відомостей про фізичну особу;</p>

                        <p><b>персональні дані&nbsp;—</b> відомості чи&nbsp;сукупність відомостей про фізичну особу, яка
                            ідентифікована або може бути конкретно ідентифікована;</p>

                        <p><b>розпорядник бази персональних даних&nbsp;—</b> фізична чи&nbsp;юридична особа, якій
                            володільцем
                            бази персональних даних або законом надано право обробляти ці&nbsp;дані.
                            Не&nbsp;є&nbsp;розпорядником бази персональних даних особа, якій володільцем та/або
                            розпорядником
                            бази персональних даних доручено здійснювати роботи технічного характеру з&nbsp;базою
                            персональних
                            даних без доступу до&nbsp;змісту персональних даних;</p>

                        <p><b>суб’єкт персональних даних&nbsp;—</b> фізична особа, стосовно якої відповідно до&nbsp;закону
                            здійснюється обробка її&nbsp;персональних даних;</p>

